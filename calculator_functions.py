def add(num1, num2):
    """Takes two numbers num1 and num2 as input and returns their sum"""
    return num1 + num2


def subtract(num1, num2):
    """Takes two numbers num1 and num2 as input and returns the value obtained when num2 is subtracted
     from num1"""
    return num1 - num2


def multiply(num1, num2):
    """Takes two numbers num1 and num2 as input and returns their multiplication"""
    return num1 * num2


def divide(num1, num2):
    """Takes two numbers num1 and num2 as input and returns the value obtained when num1 is divided by
    num2"""
    return num1 / num2
